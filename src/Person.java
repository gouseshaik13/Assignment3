class Person {
	private int age;
	private String name;
	private float price;
	public int getage() {
		return age;

	}
	public void setage(int age) {
		this.age =age;

	}
	public String getname() {
		return name;

	}
	public void setname(String name) {
		this.name = name;

	}
	public float getprice() {
		return price;
	}
	public void setprice(float price) {
		this.price = price;

	}

}